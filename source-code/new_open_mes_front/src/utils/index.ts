export const emptyValue = (value: any) => {
  switch (typeof value) {
    case 'object':
      if (Array.isArray(value)) {
        return []
      } else {
        return {
          dd: () => {}
        }
      }
    case 'string':
      return ''
    default:
      return value
  }
}

/**
 * 时间戳转时间
 * @param {Number} date 时间戳
 * @param {String} format 需要的格式
 * @return {String} 返回格式化完成时间
 */
interface timeFormat {
  (date: Date | any, format: string): String
}
export const timeFormat: timeFormat = (date, format) => {
  date = new Date(date)
  if (date == 'Invalid Date') {
    return ''
  }
  const map: any = {
    M: date.getMonth() + 1, //月份
    d: date.getDate(), //日
    h: date.getHours(), //小时
    m: date.getMinutes(), //分
    s: date.getSeconds(), //秒
    q: Math.floor((date.getMonth() + 3) / 3), //季度
    S: date.getMilliseconds() //毫秒
  }
  format = format.replace(/([yMdhmsqS])+/g, function (all: any, t: any) {
    let v = map[t]
    if (v !== undefined) {
      if (all.length > 1) {
        v = '0' + v
        v = v.substr(v.length - 2)
      }
      return v
    } else if (t === 'y') {
      return (date.getFullYear() + '').substr(4 - all.length)
    }
    return all
  })
  return format
}

//如果输入是数字且是整数，则直接返回原有数字；如果输入是数字且有小数，则保留三位小数；
// 如果输入是字符串且可以转换为数字，则按照同样的逻辑处理；如果输入无法处理，则原样返回。
export const formatNumber = (input: string | number) => {
  if (typeof input === 'number') {
    if (Number.isInteger(input)) {
      return input
    } else {
      return input.toFixed(3)
    }
  } else if (typeof input === 'string') {
    const numberValue = parseFloat(input)
    if (!isNaN(numberValue)) {
      if (Number.isInteger(numberValue)) {
        return numberValue
      } else {
        return numberValue.toFixed(3)
      }
    }
  }
  return input
}
